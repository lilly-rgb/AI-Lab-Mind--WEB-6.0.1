export let currentLang = 'es';

function updateDOM() {
    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });
    
    document.querySelectorAll('[data-lang-title-key]').forEach(element => {
        const key = element.getAttribute('data-lang-title-key');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.title = translations[currentLang][key];
        }
    });

    document.querySelectorAll('[data-lang-placeholder-key]').forEach(element => {
      const key = element.getAttribute('data-lang-placeholder-key');
      if (translations[currentLang] && translations[currentLang][key]) {
          element.placeholder = translations[currentLang][key];
      }
    });

    document.querySelectorAll('[data-lang-href-key]').forEach(element => {
      const key = element.getAttribute('data-lang-href-key');
      if(translations[currentLang][key]) {
        element.href = translations[currentLang][key];
      }
    });

    document.querySelectorAll('[data-lang-aria-label-key]').forEach(element => {
      const key = element.getAttribute('data-lang-aria-label-key');
      if(translations[currentLang][key]) {
          element.setAttribute('aria-label', translations[currentLang][key]);
      }
    });
    
    const consentDesc = document.querySelector('[data-lang-key="consent.description"]');
    if (consentDesc) {
      consentDesc.textContent = translations[currentLang]['consent.description'] + ' ';
      const link = consentDesc.nextElementSibling;
      if (link && link.dataset.langKey === 'consent.cookie-policy-link') {
          link.textContent = translations[currentLang]['consent.cookie-policy-link'];
      }
    }

    document.getElementById('lang-toggle-text').textContent = currentLang === 'es' ? 'EN' : 'ES';

    // Dispatch event to notify other modules of the language change
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: currentLang } }));
}

export function getTranslation(key) {
    return translations[currentLang][key] || key;
}

export function setLanguage(lang, force = false) {
    if (!translations[lang] || (currentLang === lang && !force)) return;
    currentLang = lang;
    document.documentElement.lang = lang;
    updateDOM();
}

export function initLanguage() {
    const langToggleButton = document.getElementById('lang-toggle');
    langToggleButton.addEventListener('click', () => {
        const newLang = currentLang === 'es' ? 'en' : 'es';
        setLanguage(newLang);
    });

    const userLang = navigator.language.split('-')[0];
    const initialLang = ['es', 'en'].includes(userLang) ? userLang : 'es';
    setLanguage(initialLang, true);
}